package com.javaex.ex01;

public class Ex09 {

	public static void main(String[] args) {
		
		//관계 연산자--> 결과값은 true,false로만
		int n1=5;
		int n2=3;
		
		System.out.println(n1<n2);
		System.out.println(n1>n2);
		System.out.println(n1<=n2);
		System.out.println(3<=n2);
		System.out.println(n1>=n2);
		System.out.println(3>=n2);
		System.out.println(n1==n2);
		System.out.println(n1==5);
		System.out.println(n1==3);
		System.out.println(n1!=3);

	}

}
